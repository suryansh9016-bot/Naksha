/** Convert palette to legacy ThemeColors shape for screens that use it */
export function paletteToThemeColors(p: Palette): ThemeColors {
  return {
    bg: p.bg,
    bgGrad: `linear-gradient(180deg, ${p.bg} 0%, ${p.bg} 100%)`,
    card: p.surface,
    cardShadowDark: "rgba(0,0,0,0.5)",
    cardShadowLight: "rgba(255,255,255,0.03)",
    text: p.text,
    textMuted: p.textMuted,
    accent: p.accent,
    accentText: p.bg,
    insetShadowDark: "rgba(0,0,0,0.4)",
    insetShadowLight: "rgba(255,255,255,0.03)",
    starColor: "rgba(200,220,255,0.9)",
  };
}
